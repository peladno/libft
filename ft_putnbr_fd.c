#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	nbr;
	char	*base;

	base = "0123456789";
	nbr = (long)n;
	if (nbr < 0)
	{
		ft_putchar_fd('-', fd);
		nbr = -nbr;
	}
	if (nbr > 9)
		ft_putnbr_fd(nbr / 10, fd);
	ft_putchar_fd(base[nbr % 10], fd);
}
